    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        boolean checker = false;
        try
        {
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/apulibrary_db","bran", "bran");
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("select * from books");
            String isbn = dtm.getValueAt(row,0).toString();
            if(flag == 1)
            {        
                while(rs.next()){
                    if(isbn.equals(rs.getString(1)) && rs.getBoolean(6)){
                        checker = true;
                    }
                }
                if(checker){
                    checker = false;
                    conn.setAutoCommit(false);
                    PreparedStatement pstmt = conn.prepareStatement("insert into borrowlist(TP_NO, Book)"
                            + "values (?,?)");
                    PreparedStatement pstmt2 = conn.prepareStatement("update books set Availability = ? where ISBN = ? ");
                    pstmt.setString(1,jLabel3.getText());
                    pstmt.setString(2,dtm.getValueAt(row,1).toString());
                    pstmt2.setBoolean(1, checker);
                    pstmt2.setString(2,dtm.getValueAt(row,0).toString());
                    pstmt.executeUpdate();
                    pstmt2.executeUpdate();
                    conn.commit();
                    conn.close();
                    JOptionPane.showMessageDialog(null, "Booked successfully");
                }
                 else{
                    JOptionPane.showMessageDialog(null, "Not available");
                 }  
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No books selected");
            }
        }
        catch(Exception ex)
        {
            Logger.getLogger(register_frame.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_jButton3ActionPerformed

