    private class fillTable {
        public void fillRows(){
        ArrayList<Book> books = new ArrayList<Book>();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        try{
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/apulibrary_db","bran", "bran");
            AddInterface object = (AddInterface)Naming.lookup("rmi://localhost:1044/displayBooks");
            books = object.displayBooks();
            for(int i =0; i < books.size(); i++){
                if(books.get(i).getbool()){
                    int isbn = books.get(i).getisbn();
                    String name = books.get(i).getname();
                    String author = books.get(i).getauthor();
                    String publisher = books.get(i).getpublisher();
                    String date = books.get(i).getdate();
                    Object[] data = {isbn, name, author, publisher, date};
                    dtm.addRow(data);
                } 
            }
        }
        catch(RemoteException | NotBoundException | MalformedURLException | SQLException ex){
            Logger.getLogger(login_frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }
    
